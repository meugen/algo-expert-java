    static List<List<Integer>> parseMatrix(String json) {
        JsonArray array = new Gson().fromJson(json, JsonElement.class)
                .getAsJsonObject().getAsJsonArray("matrix");
        List<List<Integer>> matrix = new ArrayList<>();
        for (JsonElement row : array) {
            List<Integer> matrixRow = new ArrayList<>();
            for (JsonElement item : row.getAsJsonArray()) {
                matrixRow.add(item.getAsInt());
            }
            matrix.add(matrixRow);
        }
        return matrix;
    }
